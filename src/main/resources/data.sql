DROP TABLE IF EXISTS pgw_transactions;

CREATE TABLE pgw_transactions (
TRANSFER_ID INT PRIMARY KEY,
TRANSFER_AMOUNT INT,
SOURCE_ACCOUNT VARCHAR(250),
TARGET_ACCOUNT VARCHAR(250),
PAYMENT_CODE VARCHAR(250),
SERVICE_FEE INT,
BANK VARCHAR(250),
SENDER_FIRST_NAME VARCHAR(250),
SENDER_LAST_NAME VARCHAR(250),
SENDER_MIDDLE_NAME VARCHAR(250),
BUSINESS_NAME VARCHAR(250),
SENDER_MOBILE_NUMBER VARCHAR(250),
SENDER_EMAIL_ADDRESS VARCHAR(250),
ADDRESS VARCHAR(250),
PURPOSE VARCHAR(250),
BENEFICIARY_TYPE VARCHAR(250),
RECIPIENT_FIRST_NAME VARCHAR(250),
RECIPIENT_LAST_NAME VARCHAR(250),
RECIPIENT_MIDDLE_NAME VARCHAR(250),
RECIPIENT_MOBILE_NUMBER VARCHAR(250),
RECIPIENT_EMAIL_ADDRESS VARCHAR(250),
PRODUCT_CODE VARCHAR(250),
EXTERNAL_ID VARCHAR(250),
CURRENCY_CODE VARCHAR(250),
BRANCH_CODE VARCHAR(250),
CIF_NO VARCHAR(250),
RECIPIENT_EMAIL_NOTIF VARCHAR(250),
RECIPIENT_SMS_NOTIF VARCHAR(250),
SENDER_EMAIL_NOTIF VARCHAR(250),
SENDER_SMS_NOTIF VARCHAR(250),
TRANSFER_STATUS VARCHAR(250),
TRANSACTION_ID VARCHAR(250),
SOURCE_ACCOUNT_NICKNAME VARCHAR(250),
TARGET_ACCOUNT_NICKNAME VARCHAR(250),
REMARKS VARCHAR(250),
OWNER VARCHAR(250),
DATE_CREATED TIMESTAMP,
DATE_MODIFIED TIMESTAMP,
TRANSACTION_STATUS_DESCRIPTION VARCHAR(250),
SENDER_ADDRESS VARCHAR(250),
CHANNEL_TRANSACTION VARCHAR(250),
TRACE_NUMBER VARCHAR(250),
PAYMENT_TYPE VARCHAR(250),
GUID VARCHAR(250),
LOYALTY_CARD_NUMBER VARCHAR(250),
RECEIVER_NATIONALITY VARCHAR(250),
SENDER_NATIONALITY VARCHAR(250),
SENDER_BIRTH_DATE VARCHAR(250),
SOURCE_OF_FUNDS VARCHAR(250),
RELATIONSHIP_TO_BENEFICIARY VARCHAR(250),
utility_code VARCHAR(250),
source_identifier VARCHAR(250),
subscriber_number VARCHAR(250),
pay_partner_reference VARCHAR(250),
client_reference VARCHAR(250),
biller_reference VARCHAR(250)
);

